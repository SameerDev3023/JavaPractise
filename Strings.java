public class Strings {
    
    public static void main(String args[]){
        String MyName = "My Name is Mohammad Sameer";

        // String Methods === .length() , .toUpperCase(), toLowerCase();
            int StringLength =  MyName.length(); // Get String length
            String UpperCaseString = MyName.toUpperCase(); // covert string to upper letters.
            String LowerCaseString = MyName.toLowerCase();// convert string to lower letters.
            

            System.out.println(MyName);
            System.out.println(StringLength);
            System.out.println(UpperCaseString);
            System.out.println(LowerCaseString);



    }
}
